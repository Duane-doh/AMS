				<p  style="margin-left:25px;">
				•<span style="margin-left:15px;">The special emergency leave can be applied for a maximum of five (5) straight working days or staggered basis within thirty (30) days from the actual occurrence of the natural calamity/disaster. Said privilege shall be enjoyed once a year, not in every instance of calamity or disaster. <br></span>
				•<span style="margin-left:15px;">The head of office shall take full responsibility for the grant of special emergency leave and verification of the employee’s eligibility to be granted thereof.  Said verification shall include: validation of place of residence based on latest available records of the affected employee; verification that the place of residence is covered in the declaration of calamity area by the proper government agency; and such other proofs as may be necessary.</span>
				</p>
				
