				<p  style="margin-left:25px;">
					•<span style="margin-left:15px;">Application shall be made within one (1) week from the time of the accident except when a longer period is warranted. </span> <br> 
					•<span style="margin-left:15px;">Letter request supported by relevant reports such as the police report, if any, </span> <br>
					•<span style="margin-left:15px;">Medical certificate on the nature of the injuries, the course of treatment involved, and the need to undergo rest, recuperation, and rehabilitation, as the case may be. </span> <br>
					•<span style="margin-left:15px;">Written concurrence of a government physician should be obtained relative to the recommendation for rehabilitation if the attending physician is a private practitioner, particularly on the duration of the period of rehabilitation. </span>

				</p>				
