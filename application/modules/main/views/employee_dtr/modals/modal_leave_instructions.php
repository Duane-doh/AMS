				<p  style="margin-left:25px;">
					•<span style="margin-left:15px;">It shall be filed in advance or immediately upon the woman employee’s return from such leave. <br></span>  
					•<span style="margin-left:15px;">It shall be accompanied by any of the following supporting documents:</span>
				</p>
